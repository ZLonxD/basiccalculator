num1 = float(input("Sayı Giriniz :"))
op = input("İşlem Giriniz :")
num2 = float(input("Sayı Giriniz :"))


if op =="+":
    print(num1 + num2)

elif op =="-":
    print(num1 - num2)

elif  op =="*":
    print(num1 * num2)

elif op =="/":
    print(num1 / num2)

else :
    print("Hata!")
